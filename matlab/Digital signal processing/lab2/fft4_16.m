function [X]=fft4_16(x)
X=zeros(1,16);
N=16;
W=exp(-1j*2*pi/N);
W4=dftmtx(4);

x0=[x(1);x(5);x(9);x(13)];
x1=[x(2);x(6);x(10);x(14)];
x2=[x(3);x(7);x(11);x(15)];
x3=[x(4);x(8);x(12);x(16)];

X0=W4*x0;
X1=W4*x1;
X2=W4*x2;
X3=W4*x3;

t0=W4*[X0(1);X1(1);X2(1);X3(1)];
t1=W4*[X0(1+1);(W^1)*X1(1+1);(W^(2*1))*X2(1+1);(W^(3*1))*X3(1+1)];
t2=W4*[X0(2+1);(W^2)*X1(2+1);(W^(2*2))*X2(2+1);(W^(3*2))*X3(2+1)];
t3=W4*[X0(3+1);(W^3)*X1(3+1);(W^(2*3))*X2(3+1);(W^(3*3))*X3(3+1)];

X(1)=t0(1);X(2)=t1(1);X(3)=t2(1);X(4)=t3(1);
X(5)=t0(2);X(6)=t1(2);X(7)=t2(2);X(8)=t3(2);
X(9)=t0(3);X(10)=t1(3);X(11)=t2(3);X(12)=t3(3);
X(13)=t0(4);X(14)=t1(4);X(15)=t2(4);X(16)=t3(4);





